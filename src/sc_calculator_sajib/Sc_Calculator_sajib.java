           private static int precedence(char operator) {
            switch (operator) {
                case '+':
                case '-': return 1;
                case '*':
                case '/': return 2;
                case '%': return 2;
                case '^': return 3; // Exponentiation has higher precedence
                default: return -1;
            }
        }

        private static boolean isOperator(char c) {
            return c == '+' || c == '-' || c == '*' || c == '/' || c == '%' || c == '^';
        }

        private static double applyOperation(char operator, double b, double a) {
            switch (operator) {
                case '+': return a + b;
                case '-': return a - b;
                case '*': return a * b;
                case '/': 
                    if (b == 0) throw new ArithmeticException("Cannot divide by zero");
                    return a / b;
                case '%': return a % b;
                case '^': return Math.pow(a, b); // Power operation
                default: throw new IllegalArgumentException("Invalid operator");
            }
        }
 
            
            
            
            
        private static double evaluate(String expression) {
            
          // Stacks for numbers and operators
          if (expression.startsWith("-")) {
               expression = "0" + expression; // Prepend "0" to handle the negative start
          }
          
           expression = expression.trim();
        if (expression.isEmpty()) {
            throw new IllegalArgumentException("Invalid expression: Expression cannot be empty.");
        }




        
        
if (expression.startsWith("Bin(") && expression.endsWith(")")) {
        String inside = expression.substring(4, expression.length() - 1).trim();
        double result = evaluate(inside); // Evaluate inside Bin(...)
        
        // Ensure the result is an integer for binary conversion
        if (result % 1 != 0) {
            throw new IllegalArgumentException("Binary conversion requires an integer.");
        }
        String binaryResult = Integer.toBinaryString((int) result);
        return Double.parseDouble(binaryResult);  // Convert binary string back to decimal
    }
    if (expression.startsWith("sin(") && expression.endsWith(")")) {
        String inside = expression.substring(4, expression.length() - 1).trim();
        double result = evaluate(inside);
        return Math.sin(Math.toRadians(result));  // Convert to radians before applying sin()
    }

    if (expression.startsWith("cos(") && expression.endsWith(")")) {
        String inside = expression.substring(4, expression.length() - 1).trim();
        double result = evaluate(inside);
        
        // Handle cos(90), cos(270), etc. correctly (it should return 0)
        if (result % 360 == 90 || result % 360 == 270) {
            return 0;  // cos(90) or cos(270) is 0
        }
        return Math.cos(Math.toRadians(result));  // Convert to radians before applying cos()
    }

    if (expression.startsWith("tan(") && expression.endsWith(")")) {
        String inside = expression.substring(4, expression.length() - 1).trim();
        double result = evaluate(inside);
        
        // Check for values where tan(x) goes to infinity (like 90, 270, etc.)
        if (result % 180 == 90) {  // tan(90), tan(270), tan(450), etc. go to infinity
            throw new ArithmeticException("tan(" + result + ") is undefined (infinity).");
        }
        return Math.tan(Math.toRadians(result)); // Convert to radians before applying tan()
    }

            if (expression.startsWith("cosec(") && expression.endsWith(")")) {
            String inside = expression.substring(6, expression.length() - 1).trim();
            double value = evaluate(inside); // Evaluate the argument
            double sineValue = Math.sin(Math.toRadians(value)); // sin(x) in radians
            if (Math.abs(sineValue) < 1e-10) { // Avoid division by zero with a small tolerance
                throw new ArithmeticException("cosec(" + value + ") is undefined.");
            }
            return 1 / sineValue; // Return cosec(x)
            }


            if (expression.startsWith("sec(") && expression.endsWith(")")) {
            String inside = expression.substring(4, expression.length() - 1).trim();
            double value = evaluate(inside); // Evaluate the argument
            double cosineValue = Math.cos(Math.toRadians(value)); // cos(x) in radians
            if (Math.abs(cosineValue) < 1e-10) { // Avoid division by zero with a small tolerance
                throw new ArithmeticException("sec(" + value + ") is undefined.");
            }
            return 1 / cosineValue; // Return sec(x)
            }



        if (expression.startsWith("cot(") && expression.endsWith(")")) {
        // Extract the value inside cot(...)
        String inside = expression.substring(4, expression.length() - 1).trim();
        double value = evaluate(inside); // Recursively evaluate the argument inside cot()

        // Normalize the angle to handle periodicity
        value = value % 360;
        if (value < 0) {
            value += 360; // Ensure the angle is positive
        }

        // Handle special cases for cot(x)
        if (value == 90 || value == 270) {
            return Double.POSITIVE_INFINITY; // cot(90) and cot(270) are infinity
        }

        if (value % 180 == 0) { // cot(0), cot(180), cot(360), etc. are undefined
            throw new ArithmeticException("cot(" + value + ") is undefined.");
        }

        // Regular computation for cot(x)
        double tangentValue = Math.tan(Math.toRadians(value));
        if (Math.abs(tangentValue) < 1e-10) { // Handle near-zero tan(x)
            throw new ArithmeticException("cot(" + value + ") is undefined.");
        }
        return 1 / tangentValue; // Compute cot(x) = 1 / tan(x)
    }

        


        // Handle 'nP' for permutations (nPr)
        if (expression.contains("P")) {
            int pIndex = expression.indexOf("P");
            double r = Double.parseDouble(expression.substring(pIndex + 1));
            double n = Double.parseDouble(expression.substring(0, pIndex));
            return nPr((int) n, (int) r);
        }

        // Handle 'nC' for combinations (nCr)
        if (expression.contains("C")) {
            int cIndex = expression.indexOf("C");
            double r = Double.parseDouble(expression.substring(cIndex + 1));
            double n = Double.parseDouble(expression.substring(0, cIndex));
            return nCr((int) n, (int) r);
        }
        if (expression.contains("E")) {
        String[] parts = expression.split("E");
        if (parts.length == 2) {
            double base = evaluate(parts[0]); 
            double exponent = evaluate(parts[1]); 
            return base * Math.pow(10, exponent);
        }
        }
        if (expression.contains("^2")) {
        int idx = expression.indexOf("^2");
        double value = evaluate(expression.substring(0, idx));
        return value * value;
        }
        if (expression.contains("^3")) {
        int idx = expression.indexOf("^3");
        double value = evaluate(expression.substring(0, idx)); // Get the base
        return value * value * value; // Calculate x^3
    }
        
        
        
        
       

    
    if (expression.contains("2^")) {
        int idx = expression.indexOf("2^");
        double exponent = evaluate(expression.substring(idx + 2)); // Get the exponent
        return Math.pow(2, exponent); // 2 raised to the power of x
    }

    if (expression.contains("^")) {
        int idx = expression.indexOf("^");
        double base = evaluate(expression.substring(0, idx)); // Get the base
        double exponent = evaluate(expression.substring(idx + 1)); // Get the exponent
        return Math.pow(base, exponent); // Calculate x^n
    }
    
    
    
    
    if (expression.startsWith("Bin(") && expression.endsWith(")")) {
        // Extract the argument inside "Bin(...)"
        String inside = expression.substring(4, expression.length() - 1).trim();

        // Evaluate the argument recursively
        double result = evaluate(inside);

        // Ensure the result is an integer for binary conversion
        if (result % 1 != 0) {
            throw new IllegalArgumentException("Binary conversion requires an integer.");
        }

        // Return the binary result as a decimal equivalent (for display in text field)
        String binaryResult = Integer.toBinaryString((int) result);
        System.out.println("Binary result: " + binaryResult); // Debugging
        return Double.parseDouble(binaryResult); // Convert binary string back to decimal
    }
    
    
    
          
          Stack<Double> numbers = new Stack<>();
          Stack<Character> operators = new Stack<>();

          for (int i = 0; i < expression.length(); i++) {
              char c = expression.charAt(i);

              // Handle numbers
              if (Character.isDigit(c) || c == '.') {
                  StringBuilder number = new StringBuilder();

                  while (i < expression.length() && (Character.isDigit(expression.charAt(i)) || expression.charAt(i) == '.')) {
                      number.append(expression.charAt(i));
                      i++;
                  }
                  i--; // Step back for loop increment
                  numbers.push(Double.parseDouble(number.toString()));
              }  else if (expression.startsWith("log(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 3);
            double value = evaluate(expression.substring(i + 4, endIndex)); // Exclude "log("
            if (value <= 0) throw new ArithmeticException("Logarithm undefined for non-positive numbers");
            numbers.push(Math.log10(value)); // Logarithm base 10
            i = endIndex;

        // Handle 'ln' (natural logarithm)
        } else if (expression.startsWith("ln(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 2);
            double value = evaluate(expression.substring(i + 3, endIndex)); // Exclude "ln("
            if (value <= 0) throw new ArithmeticException("Natural logarithm undefined for non-positive numbers");
            numbers.push(Math.log(value)); // Natural logarithm (base e)
            i = endIndex;

        // Handle trigonometric and other functions (e.g., sin, cos, tan)
        } else if (expression.startsWith("sin(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 3);
            double value = evaluate(expression.substring(i + 4, endIndex));
            numbers.push(Math.sin(Math.toRadians(value)));
            i = endIndex;
        } else if (expression.startsWith("cos(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 3);
            double value = evaluate(expression.substring(i + 4, endIndex));
            numbers.push(Math.cos(Math.toRadians(value)));
            i = endIndex;
        } else if (expression.startsWith("tan(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 3);
            double value = evaluate(expression.substring(i + 4, endIndex));
            numbers.push(Math.tan(Math.toRadians(value)));
            i = endIndex;

        // Handle other operations (e.g., sin⁻¹, cos⁻¹, tan⁻¹, etc.)
        } else if (expression.startsWith("sin⁻¹(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 5);
            double value = evaluate(expression.substring(i + 5, endIndex));
            numbers.push(Math.toDegrees(Math.asin(value)));
            i = endIndex;
        } else if (expression.startsWith("cos⁻¹(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 5);
            double value = evaluate(expression.substring(i + 5, endIndex));
            numbers.push(Math.toDegrees(Math.acos(value)));
            i = endIndex;
        } else if (expression.startsWith("tan⁻¹(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 5);
            double value = evaluate(expression.substring(i + 5, endIndex));
            numbers.push(Math.toDegrees(Math.atan(value)));
            i = endIndex;

        // Handle operators
        }  else if (expression.startsWith("1/(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 2);
            double value = evaluate(expression.substring(i + 2, endIndex));
            numbers.push(1 / value);
            i = endIndex;
        } else if (expression.startsWith("π", i)) {
            numbers.push(Math.PI);
        } else if (expression.startsWith("e", i)) {
            numbers.push(Math.E);
        }
              else if (expression.startsWith("1/(", i)) {
                int endIndex = findClosingParenthesis(expression, i + 2);
                double value = evaluate(expression.substring(i + 2, endIndex));
                numbers.push(1 / value);
                i = endIndex;
            }else if (expression.startsWith("√(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 1);
            double value = evaluate(expression.substring(i + 2, endIndex)); // Exclude "√("
            numbers.push(Math.sqrt(value)); // Calculate square root
            i = endIndex;

       
        } else if (expression.startsWith("³√(", i)) {
            int endIndex = findClosingParenthesis(expression, i + 2);
            double value = evaluate(expression.substring(i + 3, endIndex)); // Exclude "³√("
            numbers.push(Math.cbrt(value)); // Calculate cube root
            i = endIndex;

        
        } else if (expression.startsWith("log(", i)) {
                int endIndex = findClosingParenthesis(expression, i + 3);
                double value = evaluate(expression.substring(i + 3, endIndex));
                numbers.push(Math.log10(value));
                i = endIndex;
            } else if (expression.startsWith("ln(", i)) {
                int endIndex = findClosingParenthesis(expression, i + 2);
                double value = evaluate(expression.substring(i + 2, endIndex));
                numbers.push(Math.log(value));
                i = endIndex;
            }else if (expression.startsWith("P", i)) {
            int endIndex = i + 1;
            double r = numbers.pop();
            double n = numbers.pop();
            numbers.push(nPr((int) n, (int) r)); // Calculate nPr
            i = endIndex;

        // Handle 'nC' for combinations (nCr)
        } else if (expression.startsWith("C", i)) {
            int endIndex = i + 1;
            double r = numbers.pop();
            double n = numbers.pop();
            numbers.push(nCr((int) n, (int) r)); // Calculate nCr
            i = endIndex;

        // Handle 'log' (logarithm base 10)
        }else if (c == '%') {
            double value = numbers.pop();
            numbers.push(value / 100); // Percentage is the number divided by 100

            // Handle factorial (!)
            }else if (c == '!') {
                // The previous number should be factorialized
            double value = numbers.pop();
            if (value != (int) value || value < 0) {
                throw new ArithmeticException("Factorial is only defined for non-negative integers");
            }
            numbers.push(factorial((int) value));

        // Handle opening parenthesis
        }
              else if (c == '(') {
                  // Handle opening parenthesis
                  operators.push(c);
              } else if (c == ')') {
                  // Handle closing parenthesis
                  while (!operators.isEmpty() && operators.peek() != '(') {
                      numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
                  }
                  operators.pop(); // Remove '('
              } else if (isOperator(c)) {
                  // Handle operators based on precedence
                  while (!operators.isEmpty() && precedence(c) <= precedence(operators.peek())) {
                      numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
                  }
                  operators.push(c);
              } 
          }

          // Process remaining operators
          while (!operators.isEmpty()) {
              numbers.push(applyOperation(operators.pop(), numbers.pop(), numbers.pop()));
          }

          return numbers.pop();
      }
            
            
            private static int findClosingParenthesis(String expression, int startIndex) {
                int balance = 0;
                for (int i = startIndex; i < expression.length(); i++) {
                    if (expression.charAt(i) == '(') {
                        balance++;
                    } else if (expression.charAt(i) == ')') {
                        balance--;
                        if (balance == 0) {
                            return i;
                        }
                    }
                }
                return -1; // If no closing parenthesis is found
            }
            
            


           
            private static double factorial(int n) {
                if (n < 0) throw new IllegalArgumentException("Factorial undefined for negative numbers");
                double result = 1;
                for (int i = 1; i <= n; i++) {
                    result *= i;
                }
                return result;
            }

            // Permutation (nPr)
            private static double nPr(int n, int r) {
                if(r>n) throw new IllegalArgumentException("r can't be greater than n.");
                return factorial(n) / factorial(n - r);
            }

            // Combination (nCr)
            private static double nCr(int n, int r) {
                if(r>n) throw new IllegalArgumentException("r can't be greater than n.");
                return factorial(n) / (factorial(r) * factorial(n - r));
            }
  
                  
           
