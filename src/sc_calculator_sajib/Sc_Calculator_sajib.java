    private void EqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualActionPerformed

                           try {
                                String expression = jTextField1.getText().trim();
                                String adjustedExpression = handleImplicitMultiplication(expression); // Preprocess
                                double result = evaluate(adjustedExpression); // Evaluate the adjusted expression
                                lastAnswer = result;
                                    jTextField1.setText(String.valueOf(result));
                                } catch (ArithmeticException e) {
                                    jTextField1.setText("Math Error: " + e.getMessage());
                                } catch (IllegalArgumentException e) {
                                    jTextField1.setText("Syntax Error: " + e.getMessage());
                                } catch (Exception e) {
                                    jTextField1.setText("Error");
                            }

        
    }//GEN-LAST:event_EqualActionPerformed

    
    
    
    
    private String handleImplicitMultiplication(String expression) {
    StringBuilder updatedExpression = new StringBuilder();
    for (int i = 0; i < expression.length(); i++) {
        char current = expression.charAt(i);
        updatedExpression.append(current);

        // Look ahead to the next character
        if (i < expression.length() - 1) {
            char next = expression.charAt(i + 1);

            // Case 1: Digit or closing parenthesis followed by an opening parenthesis
            if ((Character.isDigit(current) || current == ')') && next == '(') {
                updatedExpression.append('*');
            }
            // Case 2: Function (e.g., sin, cos) followed by a parenthesis
            if (Character.isLetter(current) && next == '(') {
                updatedExpression.append('*');
            }
        }
    }
    return updatedExpression.toString();
}

    
    
    
    
    
    
    
    
    
    
    
